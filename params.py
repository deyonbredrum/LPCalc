def get_lightning_level(N, attr_type):
    if attr_type in ["explosive", "explosive_zone"]:
        return 1
    if attr_type in ["explosive_zone1", "crowded", "important", "heritage", "office", "comm", "station", "airport"]:
        if N > 0.05:
            return 2
        else:
            return 3
    if attr_type == "normal":
        if N > 0.25:
            return 2
        elif N > 0.05:
            return 3
        else:
            return None
    return 3

